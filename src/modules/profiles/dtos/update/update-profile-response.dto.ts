import { ObjectType, Field } from '@nestjs/graphql';

@ObjectType()
export class UpdateProfileResponseDto {
  @Field()
  idProfiles: string;

  @Field({ nullable: true })
  phone?: string;

  @Field({ nullable: true })
  currentWeight?: number;

  @Field({ nullable: true })
  currentHeight?: number;

  @Field({ nullable: true })
  currentImc?: number;

  @Field({ nullable: true })
  birthDate?: string;

  @Field({ nullable: true })
  sex?: 'male' | 'female' | 'other';

  @Field({ nullable: true })
  activityLevel?: 'sedentary' | 'light' | 'moderate' | 'active' | 'very_active';

  @Field({ nullable: true })
  goal?: 'lose_weight' | 'maintain' | 'gain_weight';

  @Field()
  createdAt: Date;

  @Field()
  updatedAt: Date;
}